package gray.domain;

public class FlowContext {
    String flowId;
    FlowInput flowInput;

    public String getFlowId() {
        return flowId;
    }

    public void setFlowId(String flowId) {
        this.flowId = flowId;
    }

    public FlowInput getFlowInput() {
        return flowInput;
    }

    public void setFlowInput(FlowInput flowInput) {
        this.flowInput = flowInput;
    }
}
